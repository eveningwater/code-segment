const httpGet = (
  url: string,
  callback: (text: string) => void,
  error = console.error
) => {
  const request = new XMLHttpRequest();
  request.open("GET", url, true);
  request.onload = () => callback(request.responseText);
  request.onerror = () => error(request);
  request.send();
};

